#definisikan nilai
x = 20
y = 5
z = -6

#pertambahan
hasilPertambahan = x + y
print("hasil dari x + y = ", hasilPertambahan)

#pengurangan 
hasilPengurangan = x - y 
print("hasil dari x - y = ", hasilPengurangan)

#pengalian
hasilPengalian = x * y 
print("hasil dari x x y = ", hasilPengalian)

#pembagian 
hasilPembagian = x / y
print("hasil dari x / y = ", hasilPembagian)

#pemangkatan 
hasilPangkat = x ** y 
print("hasil dari x pangkat y = ", hasilPangkat)

#modulus sisa pembagian
hasilModulus = x % y 
print("hasil dari x modulus y = ", hasilModulus)

#floor division
hasilFloor = x // y 
print("hasil dari x floor y = ", hasilFloor)

#prioritas oprasional 
hasilPo = x ** z + x - y // x % z * y / x
print("hasilnya", hasilPo // 3)


